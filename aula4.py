# Tipos de dados int e float
# Tipo int >> Classifica os dados de caracter Número Inteiro
# O Tipo int representa todo e qualquer número inteiro, seja positivo ou negativo.
# int sem sinal é considerado POSITIVO.

# Tipo float >> Classifica numeros que tenha o ponto flutuante, ou seja, numeros decimais.
# O tipo float representa todo e qualquer numero decimal, seja positivo ou negativo.
# float sem sinal na frente é considerado POSITIVO.
# Para colocar numeros decimais dentro de uma função, deve-se usar '.' ao invés da virgula. Virgula é usada para separar informações dentro de uma função.

# A Class "Type" é usada para demonstrar a tipagem do dado, ou seja, inferir o tipo.
# Usasse normlamente dentro da função.

#Exemplo:

print(type("Higor"),type(27),type('Loiro'))
print(type(1997))
print(type(-0.9))